// Export utility functions
export const getENotesBySubject = (subject) => {
  return eNotesData.filter(note => note.subject === subject);
};

export const getENotesByDifficulty = (difficulty) => {
  return eNotesData.filter(note => note.difficulty === difficulty);
};

export const searchENotes = (searchTerm) => {
  return eNotesData.filter(note => 
    note.title.toLowerCase().includes(searchTerm.toLowerCase()) ||
    note.description.toLowerCase().includes(searchTerm.toLowerCase()) ||
    note.tags.some(tag => tag.toLowerCase().includes(searchTerm.toLowerCase()))
  );
};
